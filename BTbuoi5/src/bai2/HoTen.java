package bai2;

public class HoTen {
	
	private String ho;
	private String tenlot;
	private String ten;
	
	public HoTen() {
	}

	public HoTen(String ho, String tenlot, String ten) {
		super();
		this.ho = ho;
		this.tenlot = tenlot;
		this.ten = ten;
	}

	public String getHo() {
		return ho;
	}

	public void setHo(String ho) {
		this.ho = ho;
	}

	public String getTenlot() {
		return tenlot;
	}

	public void setTenlot(String tenlot) {
		this.tenlot = tenlot;
	}

	public String getTen() {
		return ten;
	}

	public void setTen(String ten) {
		this.ten = ten;
	}
	

}
